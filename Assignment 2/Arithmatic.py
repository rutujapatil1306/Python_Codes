def Addition(No1,No2):
    Ans = No1 + No2 
    return Ans

def Substraction(No1,No2):
    if(No1 > No2):
        Ans = No1 - No2
        return Ans
    else:
        print("Enter first number greater than second number")

def Multiplication(No1,No2):
    Ans = No1 * No2
    return Ans 

def Division(No1,No2):    
    Ans = No1 / No2 
    return Ans     
